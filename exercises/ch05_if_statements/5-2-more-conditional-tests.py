# 5-2 more conditional tests

# 1 检查字符串相等和不相等
my_favorite_food = "fish and chips"
your_favorite_food = "pizza"
print(
    f"Is my favorite food == 'fish and chips'? I predict True.\n{my_favorite_food == 'fish and chips'}"
)
print(
    f"Is your favorite food != 'pizza'? I predict True.\n{your_favorite_food == 'pizza'}"
)
